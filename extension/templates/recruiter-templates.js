/**
 * recruiter-templates.js
 * Draft generation for the four email types supported by the extension.
 *
 * Load order: no dependencies — load before email-type-selector.js
 */

/** @typedef {"referral_request" | "to_recruiter" | "seeking_advice" | "ai_generated"} EmailTemplateType */

/**
 * @typedef {Object} DraftResult
 * @property {string} subject
 * @property {string} body
 */

/**
 * Generates a draft subject + body for the given contact and template type.
 *
 * @param {{ name?: string, company?: string, role?: string, email?: string }} contact
 * @param {EmailTemplateType} templateType
 * @returns {DraftResult}
 */
function generateDraft(contact, templateType) {
  const firstName = _firstName(contact?.name);
  const company   = String(contact?.company || "your company").trim();

  switch (templateType) {
    case "referral_request":
      return _referralRequest(firstName, company);
    case "to_recruiter":
      return _toRecruiter(firstName, company);
    case "seeking_advice":
      return _seekingAdvice(firstName, company);
    case "ai_generated":
      return _aiGeneratedPlaceholder(firstName, company);
    default:
      return _referralRequest(firstName, company);
  }
}

// ── Private template builders ──────────────────────────────────────────────

function _firstName(fullName) {
  const name = String(fullName || "").trim();
  if (!name) return "there";
  return name.split(/\s+/)[0];
}

function _referralRequest(firstName, company) {
  return {
    subject: `Referral Opportunity at ${company}`,
    body: `Hi ${firstName},

I hope this message finds you well. I came across your profile and noticed your work at ${company} — really impressive.

I'm currently exploring opportunities in [your field] and would love to know if there are any open roles that align with my background. If so, I'd greatly appreciate a referral or an introduction to the right person on your team.

Happy to share my resume or chat briefly at your convenience.

Thank you for your time,
[Your Name]`,
  };
}

function _toRecruiter(firstName, company) {
  return {
    subject: `Exploring Opportunities at ${company}`,
    body: `Hi ${firstName},

I came across your profile and saw that you recruit for ${company}. I'm actively looking for [role type] positions and think my background in [your skills/industry] could be a strong match.

I'd love to connect and learn more about any relevant openings you're currently working on. I've attached my resume for your reference.

Looking forward to hearing from you,
[Your Name]`,
  };
}

function _seekingAdvice(firstName, company) {
  return {
    subject: `Quick Question About Your Experience at ${company}`,
    body: `Hi ${firstName},

I hope this isn't too forward — I came across your profile and was really impressed by your career path at ${company}.

I'm currently [brief context about yourself] and would love to get 15–20 minutes of your time for an informational chat. I'm especially curious about [specific aspect of their role or company].

No agenda other than learning from someone I admire. Happy to work around your schedule entirely.

Thank you for considering it,
[Your Name]`,
  };
}

function _aiGeneratedPlaceholder(firstName, company) {
  return {
    subject: `Connecting with ${company}`,
    body: `Hi ${firstName},

[This draft will be generated by AI based on ${firstName}'s profile at ${company}. Upgrade to Pro to enable AI-generated personalised outreach.]

Best regards,
[Your Name]`,
  };
}
