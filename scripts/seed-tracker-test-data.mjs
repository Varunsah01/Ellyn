#!/usr/bin/env node

import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const DEFAULT_COUNT = 50;
const DEFAULT_OUTPUT = "tests/fixtures/tracker-seed-contacts.json";
const SEED_TAG = "tracker_seed_v2";
const STATUS_CYCLE = ["new", "contacted", "no_response", "replied"];
const COMPANIES = [
  "Stripe",
  "Notion",
  "Atlassian",
  "Figma",
  "Datadog",
  "Airbnb",
  "Canva",
  "Shopify",
];
const ROLES = [
  "Software Engineer",
  "Product Manager",
  "Engineering Manager",
  "Recruiter",
  "Staff Engineer",
  "Data Scientist",
];

function parseArg(name, fallback = "") {
  const prefixed = process.argv.find((arg) => arg.startsWith(`--${name}=`));
  if (!prefixed) return fallback;
  return prefixed.split("=")[1] || fallback;
}

function hasFlag(flag) {
  return process.argv.includes(`--${flag}`);
}

function buildDate(daysAgo) {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  return date.toISOString();
}

function buildSeedContacts(count) {
  return Array.from({ length: count }).map((_, index) => {
    const number = index + 1;
    const status = STATUS_CYCLE[index % STATUS_CYCLE.length];
    const company = COMPANIES[index % COMPANIES.length];
    const role = ROLES[index % ROLES.length];
    const firstName = `Seed${String(number).padStart(3, "0")}`;
    const lastName = "Contact";
    const email = `seed.${String(number).padStart(3, "0")}@${company.toLowerCase()}.example`;
    const createdDaysAgo = index % 35;
    const contactedDaysAgo = status === "new" ? null : (index % 18) + 1;

    return {
      firstName,
      lastName,
      fullName: `${firstName} ${lastName}`,
      company: `${company} Seed`,
      role,
      inferredEmail: email,
      status,
      notes: "Generated by tracker seed script",
      tags: [SEED_TAG, `batch_${Math.floor(index / 10) + 1}`],
      source: "tracker_seed_script",
      createdAt: buildDate(createdDaysAgo),
      lastContactedAt: contactedDaysAgo == null ? null : buildDate(contactedDaysAgo),
    };
  });
}

async function writeFixtureFile(outputPath, contacts) {
  const absolute = path.resolve(process.cwd(), outputPath);
  await mkdir(path.dirname(absolute), { recursive: true });
  await writeFile(absolute, JSON.stringify(contacts, null, 2), "utf8");
  console.log(`Wrote ${contacts.length} contacts to ${absolute}`);
}

async function fetchJson(url, init = undefined) {
  const response = await fetch(url, init);
  if (!response.ok) {
    const text = await response.text();
    throw new Error(`HTTP ${response.status} ${response.statusText}: ${text}`);
  }
  return response.json();
}

async function clearSeededContacts(apiBaseUrl) {
  const encodedSearch = encodeURIComponent("Seed");
  const payload = await fetchJson(`${apiBaseUrl}/api/contacts?limit=1000&search=${encodedSearch}`);
  const contacts = Array.isArray(payload.contacts) ? payload.contacts : [];
  const seeded = contacts.filter((item) => String(item.company || "").includes("Seed"));

  for (const contact of seeded) {
    await fetchJson(`${apiBaseUrl}/api/contacts/${contact.id}`, { method: "DELETE" });
  }

  console.log(`Cleared ${seeded.length} seeded contacts from API.`);
}

async function seedViaApi(apiBaseUrl, contacts) {
  for (const contact of contacts) {
    await fetchJson(`${apiBaseUrl}/api/contacts`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        firstName: contact.firstName,
        lastName: contact.lastName,
        company: contact.company,
        role: contact.role,
        inferredEmail: contact.inferredEmail,
        status: contact.status,
        notes: contact.notes,
        tags: contact.tags,
        source: contact.source,
      }),
    });
  }

  console.log(`Seeded ${contacts.length} contacts via ${apiBaseUrl}/api/contacts`);
}

async function main() {
  const count = Number.parseInt(parseArg("count", String(DEFAULT_COUNT)), 10);
  const mode = parseArg("mode", "fixture").toLowerCase();
  const output = parseArg("output", DEFAULT_OUTPUT);
  const apiBaseUrl = parseArg("api", "http://localhost:3000").replace(/\/$/, "");
  const clearOnly = hasFlag("clear-only");
  const clearFirst = hasFlag("clear-first");

  if (!Number.isFinite(count) || count < 0) {
    throw new Error("--count must be a non-negative number");
  }

  if (mode !== "fixture" && mode !== "api") {
    throw new Error("--mode must be either fixture or api");
  }

  if (mode === "fixture") {
    const contacts = buildSeedContacts(count);
    await writeFixtureFile(output, contacts);
    await writeFixtureFile(output.replace(".json", ".empty.json"), []);
    console.log("Generated both populated and empty fixtures for tracker testing.");
    return;
  }

  if (clearFirst || clearOnly) {
    await clearSeededContacts(apiBaseUrl);
  }
  if (clearOnly) return;

  const contacts = buildSeedContacts(count);
  await seedViaApi(apiBaseUrl, contacts);
}

main().catch((error) => {
  console.error("[seed-tracker-test-data] Failed:", error);
  process.exit(1);
});

